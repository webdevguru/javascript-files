// একটি অবজেক্ট তৈরি করা হচ্ছে
let person = {
  name: "John",      // প্রোপার্টি 'name' এর মান 'John'
  age: 30,           // প্রোপার্টি 'age' এর মান 30
  job: "developer"   // প্রোপার্টি 'job' এর মান 'developer'
};

// অবজেক্টের সমস্ত প্রোপার্টি কীগুলোর উপর লুপ করা হচ্ছে
for (let key in person) {
  // 'key' হলো প্রোপার্টির নাম এবং 'person[key]' হলো ঐ প্রোপার্টির মান
  console.log(key + ": " + person[key]);  // প্রোপার্টির নাম ও মান কনসোলে প্রিন্ট করা হচ্ছে
}

  